export function translate(locale: Locale, key: string, params?: TranslationParams) {
  const template = dictionaries[locale][key] ?? dictionaries["zh-CN"][key] ?? key
  if (!params) {
    return template
  }
  return template.replace(/\{\{(\w+)\}\}/g, (_, token: string) => {
    const value = params[token]
    return value === undefined || value === null ? "" : String(value)
  })
}

export function getTaskStatusLabel(locale: Locale, status: string) {
  const key = `status.${status}`
  const translated = translate(locale, key)
  return translated === key ? status : translated
}

export function getWorkbenchStageLabel(locale: Locale, stage: string) {
  return translate(locale, `task.workbench.stage.${stage}`)
}

